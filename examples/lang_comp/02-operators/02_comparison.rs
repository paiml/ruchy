fn main() {
    let x = 5;
    let y = 10;
    println!("Equal: {} == {} is {}", x, y, x == y);
    println!("Not equal: {} != {} is {}", x, y, x != y);
    println!("Less than: {} < {} is {}", x, y, x < y);
    println!("Greater than: {} > {} is {}", x, y, x > y);
    println!("Less than or equal: {} <= {} is {}", x, y, x <= y);
    println!("Greater than or equal: {} >= {} is {}", x, y, x >= y);
}
